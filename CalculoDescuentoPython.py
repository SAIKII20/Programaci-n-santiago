# Funci√≥n para calcular el descuento
def calcular_descuento(monto_total, porcentaje_descuento=10):
    return (porcentaje_descuento / 100) * monto_total

# Programa principal
monto1 = 100
descuento1 = calcular_descuento(monto1)
print(f"Descuento: ${descuento1}, Monto final: ${monto1 - descuento1}")

monto2 = 200
descuento2 = calcular_descuento(monto2, 15)
print(f"Descuento: ${descuento2}, Monto final: ${monto2 - descuento2}")